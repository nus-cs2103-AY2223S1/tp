package seedu.address.ui;

/**
 * Represents the state of the secondary pane.
 */
public enum SecondaryPaneState {
    /**
     * The secondary pane is showing the welcome/home page.
     */
    WELCOME,
    /**
     * The secondary pane is showing the target person.
     */
    TARGET_PERSON,
    /**
     * The secondary pane is showing the help page.
     */
    HELP,
    /**
     * The secondary pane is showing the message templates.
     */
    MESSAGE_TEMPLATES,
}
